package com.cts.cip.util;

import javax.validation.constraints.NotNull;
import javax.validation.constraints.Pattern;

public class Order {
	
	@NotNull
	private String orderNumber;
	
	
	@Pattern(regexp = "^[A-Za-z0-9]+$",message="{cip_common.order.number.pattern}")
	private String shipViaCode;
	
	private String firstName;
	private String lastName;
	
	public String getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}
	public String getShipViaCode() {
		return shipViaCode;
	}
	public void setShipViaCode(String shipViaCode) {
		this.shipViaCode = shipViaCode;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	

}
