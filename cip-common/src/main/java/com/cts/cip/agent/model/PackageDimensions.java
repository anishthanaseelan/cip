package com.cts.cip.agent.model;


public class PackageDimensions {

	public float getLength() {
		return length;
	}

	public void setLength(float length) {
		this.length = length;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	public DimensionsUOM getDimensionUOM() {
		return dimensionUOM;
	}

	public void setDimensionUOM(DimensionsUOM dimensionUOM) {
		this.dimensionUOM = dimensionUOM;
	}

	private float length;

	private float width;

	private float height;

	private DimensionsUOM dimensionUOM;

  






}
